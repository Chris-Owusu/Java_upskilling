package org.example;

public class Question1 {

    public static void main(String[] args) {
        int bankBalance = 500;

        // Perform addition
        int addValue = 250;
        bankBalance = bankBalance + addValue;

        // Perform subtraction
        int subValue = 100;
        bankBalance = bankBalance - subValue;

        System.out.println("Final bank balance: " + bankBalance);

        // Double variable
        double fahrenheit = 54;

        // Calculate Celsius
        double celsius = (fahrenheit - 32) * 5 / 9;
        System.out.println("Celsius value is: " + celsius);

    }

}
